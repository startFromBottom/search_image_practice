      when(
        client.get(
          Uri.parse(
              "${PixabayApi.baseUrl}?key=${PixabayApi.key}&q=iphone&image_type=photo"),
        ),
      ).thenAnswer(
        (_) async => http.Response(fakeJsonBody, 200),
      );

